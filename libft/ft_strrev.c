#include "libft.h"

void	ft_strrev(char *s)
{
	int		i;
	int		j;

	j = s ? ft_strlen(s) - 1 : 0;
	i = 0;
	while (s[i] && s[j] && i < j)
	{
		ft_swap(&s[i], &s[j]);
		i++;
		j--;
	}
}
