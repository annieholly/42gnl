#include "libft.h"

char	*ft_strmap(char const *s, char (*f)(char))
{
	char	*s2;
	int		i;

	if (s)
	{
		s2 = ft_strnew(ft_strlen(s));
		i = 0;
		while (s[i] && s2)
		{
			s2[i] = (*f)(s[i]);
			i++;
		}
		return (s2);
	}
	return ((void *)0);
}
